                            <h4>DDUGKY - MULTI PARTNER APPROACH</h4><br>
                            <p style="text-align:justify">DDU-GKY is mindful that it must engage the support of multiple partners to ensure success and maximize the investment made in time and resources. In order to energize and build mass support as well as create awareness amongst the rural youth, The DDU-GKY projects are market-linked and implemented in PPP mode. The involvement and partnership between civil society organizations, educational institutions, apex skill partners and regulating organizations, the Government, and Private organizations, ensure that DDU-GKY can leverage on the strengths of all and achieve transformative change.<br><br>

                                Curriculum framing and assessment support is through NCVT (National Council on Vocational Training) or SSCs (Sector Skills Councils). Industry partnerships allow access to new technology and on-the-job training. There are dedicated training hours for soft skills, spoken English, and basic computers, in residential as well as non-residential well-equipped campuses, with adequate technology as per industry standards.</p><br>

