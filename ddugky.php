                                    <p style="text-align:justify;">Deen Dayal Upadhyaya Grameen Kaushalya Yojana (DDU-GKY) is the skilling and placement initiative of the Ministry of Rural Development (MoRD), Government of India. DDU-GKY has its origins in the Aajeevika Skills programme and the ‘Special Projects’ component of the Swarnjayanti Gram Swarozgar Yojana (SGSY). The scheme focuses on catering to the occupational aspirations of rural youth and enhancing their skills for wage employment.</p>
                                    <p style="text-align:justify;">Implementation of DDU-GKY involves State Governments, Technical Support Agencies like the National Institute of Rural Development and Panchayati Raj (NIRD & PR), and Project Implementing Agencies (PIAs). </p>
                                    <p style="text-align:justify;">MoRD has notified the Guidelines and Standard Operating Procedures (SOP) to be followed while implementing the programme. It is mandatory for all project functionaries under DDU-GKY to be trained, assessed and certified in the Standard Operating Procedures. This e-learning portal is designed to facilitate self-learning of the SOP as well as certification.</p>


