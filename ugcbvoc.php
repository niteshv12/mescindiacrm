                                    <h4 class="justify">The University Grants Commission (UGC) had launched a scheme on 27 February, 2014 for skills development based higher education as part of college/university education, leading to Bachelor of Vocation (B.Voc.) degree with multiple entry and exit points. Considering the implementation modalities, the guidelines of the scheme have been revised in the year 2015.</h4>
                                    <p class="normal">The B.Voc. programme is focused on universities and colleges providing undergraduate studies which would also incorporate specific job roles and their NOSs along with broad based general education. </p>
                                </div>
                            </div> <!-- .col-sm-6 -->
                            <p class="normal">This would enable the graduates completing B.Voc to make a meaningful participation in accelerating India’s economy by gaining appropriate employment, becoming entrepreneurs and creating appropriate knowledge.</p>


