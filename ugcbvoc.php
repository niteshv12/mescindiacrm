                            <h4>The main objectives of the scheme are:-</h4>

                            <ul>
                                <li> <span></span>To provide judicious mix of skills relating to a profession and appropriate content of general education.</li>
                                <li> <span></span> To ensure that the students have adequate knowledge and skills, so that they are work ready at each exit point of the programme.</li>
                                <li> <span></span>To provide flexibility to students by means of pre-defined entry and multiple exit points.</li>
                                <li> <span></span>To integrate NSQF within the undergraduate level of higher education in order to enhance employability of the graduates and meet industry requirements. Such graduates apart from meeting the needs of local and national industry are also expected to be equipped to become part of the global workforce.</li>
                                <li> <span></span>To provide vertical mobility to students coming out of (a) 10+2 with vocational subjects; and (b)Community Colleges.</li>

                            </ul>

