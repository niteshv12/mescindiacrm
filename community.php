                                    <p style="text-align:justify;">Community Colleges (CC) to serve multiple needs including (i) career oriented education and skills to students interested in directly entering the workforce; (ii) training and education programmes for local employers; (iii) high-touch remedial education for secondary school graduates not ready to enroll in traditional colleges, giving them a path to transfer to three or four year institutions and (iv) general interest courses to the community for personal development and interest.</p>
