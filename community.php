                            <h4>The main objectives of the scheme are:-</h4>

                            <ul>
                                <li> <span></span>To make higher education relevant to the learner and the community</li>
                                <li> <span></span> To integrate relevant skills into the higher education system</li>
                                <li> <span></span>To provide skill based education to students currently pursuing higher education but actually interested in entering the workforce at the earliest opportunity</li>
                                <li> <span></span>To provide employable and certifiable skills based on National Occupational Standards (NOSs) with necessary general education to Senior Secondary School pass-outs, with general education and /or vocational education background.</li>
                                <li> <span></span>To provide for up-gradation and certification of traditional / acquired skills of the learners irrespective of their age</li>
                                <li> <span></span>To provide opportunities for community–based life-long learning by offering courses of general interest to the community for personal development and interest</li>
                                <li> <span></span> To provide opportunity for vertical mobility to move to higher education in future</li>
                                <li> <span></span>To offer bridge courses to certificate holders of general / vocational education, so as to bring them at par with appropriate NSQF level.</li>
                                <li> <span></span>To provide entrepreneurial orientation along with required skill training for self-employment and entrepreneurship development. </li>

                            </ul>
